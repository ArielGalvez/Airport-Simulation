    public TablaAviones(ArrayList<Aeropuerto> aeropuertos) {
        
        this.aeropuertos=aeropuertos;
        initComponents();
        String cabecera[]={"Ubicacion","Modelo", "Placa", "Nombre", "Capacidad", "Ocupados"};
        String datos[][]={};
        modelo = new DefaultTableModel(datos, cabecera);
        table.setModel(modelo);
        int anchos [] = {160,100,100,100,100,100};
        for(int i=0; i<6; i++){
        	table.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        for(int i=0;i<aeropuertos.size();i++){
            for(int j=0;j<aeropuertos.get(i).getAviones().size();j++){
                modelo.addRow(aeropuertos.get(i).getAviones().get(j).getForTable(aeropuertos.get(i).getNombre()));
            }
        }
    }
