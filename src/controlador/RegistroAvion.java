    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        try {
            String model= tipo.getSelectedItem().toString();
            int capacidad=Integer.parseInt(tex4.getText());
            
            if(!tex2.getText().equals("")||!tex3.getText().equals("")){
             padre.getPais().registrarAvion(padre.getId(),model, tex2.getText(), tex3.getText(), capacidad);
             padre.getPanel().actualizarAvion();
             padre.getPanel().actualizarTabla();
            }else JOptionPane.showMessageDialog(null, "Datos vacios");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Datos incorrectos");
        }
        this.dispose();
        padre.setVisibleAvion();
    }//GEN-LAST:event_btnAceptarActionPerformed

